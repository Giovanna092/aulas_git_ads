//maior que >
console.log("2 > 1 " + (2 > 1))

//maior igual a >=
console.log("2 >= 2 " + (2 >= 2))

//menor que <
console.log("2 < 1 " + (2 < 1))

//menor ou igual a <=
console.log("2 <= 2 " + (2 <= 2))

//igual a == (comparação)
console.log("2 == 2 " + (2 == "2"))

//identico ===
console.log("2 === 2 " + (2==="2"))

//diferente !=
console.log("2 != 3 " + (2 != 3))